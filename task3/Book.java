package org.example.hw230420.task3;

public class Book {
    String name;
    String author;
    Float price;
    Integer year;
    Integer pages;

    public Book(String name, String author, Float price, Integer year, Integer pages) {
        this.name = name;
        this.author = author;
        this.price = price;
        this.year = year;
        this.pages = pages;
    }
}
